    private void ZerarLista() {
    DefaultTableModel tab = (DefaultTableModel) tableProdutos.getModel();
        for (int c = 0; c < tab.getRowCount();) {
            tab.removeRow(0);
        }
    }

    private DefaultTableModel addlist(DefaultTableModel tab, Produto c) {
        tab.addRow(new String[]{c.getNome(),c.getDescricao(), c.getValorCompra(), c.getValorVenda(),
        c.getCategorias()});
        return tab; 
    
    }
