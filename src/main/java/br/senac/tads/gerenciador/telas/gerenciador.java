    public boolean validacaoCampos() {
        if (fieldNome.getText().equals("")
                || fieldDescricao.getText().equals("")
                || fieldNome.getText().equals("")
                || fieldVlrCompra.getText().equals("")
                || fieldVlrVenda.getText().equals("")
                || cbCategorias.getSelectedItem().equals("Selecione")
                ) {
            return false;
        } else {
            return true;
        }
    }
    
    public void limpaCampos() {
        fieldNome.setText("");
        fieldDescricao.setText("");
        fieldVlrCompra.setText("");
        fieldVlrVenda.setText("");
        cbCategorias.setSelectedItem(1);
        
    }
    
    public void salvaProduto(Produto produto) throws Exception {
        produto.setNome(fieldNome.getText());
        produto.setDescricao(fieldDescricao.getText());
        produto.setValorCompra(fieldVlrCompra.getText());
        produto.setValorVenda(fieldVlrVenda.getText());
        produto.setCategorias((String) cbCategorias.getSelectedItem());
        
        ServicoGerenciador.addProduto(produto);       
        
        JOptionPane.showMessageDialog(this, "Produto salvo com sucesso.");        
    }
    
